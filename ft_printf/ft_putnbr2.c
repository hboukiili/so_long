#include "../so_long.h"

int	ft_putnbr2(unsigned int n)
{
	int	x;

	x = 0;
	if (n >= 10)
	{
		x += ft_putnbr2(n / 10);
		x += ft_putchar((n % 10) + 48);
	}
	if (n < 10)
		x += ft_putchar(n + 48);
	return (x);
}
